# Function to cleanup on exit
cleanup() {
    echo -e "\nüëã Shutting down Nogram..."
    docker-compose down
    echo "‚ú® Thanks for using Nogram! ‚ú®"
    exit 0
}

# Register the cleanup function for SIGINT (Ctrl+C)
trap cleanup SIGINT

# Check if Docker is installed
if ! command -v docker &> /dev/null; then
    echo "‚ùå Docker is not installed!"
    echo "Please install Docker first:"
    echo "üîó https://www.docker.com/products/docker-desktop/"
    exit 1
fi

# Check if Docker is running
if ! docker info &> /dev/null; then
    echo "‚ùå Docker is not running!"
    echo "Please start Docker Desktop and try again"
    exit 1
fi

echo "üöÄ Starting Nogram..."

# Start the application
docker-compose up --build -d

echo "‚è≥ Waiting for services to be ready..."

# Function to check if URL is responding
check_url() {
    curl -s -o /dev/null -w ''%{http_code}'' "$1"
}

# Wait for frontend to be available
max_attempts=60
attempt=1
echo "Waiting for application to start..."
while [ $attempt -le $max_attempts ]; do
    status_code=$(check_url "http://localhost:5173")
    if [ "$status_code" = "200" ]; then
        echo "‚úÖ Application is ready!"
        
        # Open browser based on operating system
        case "$(uname -s)" in
            Darwin*)    open http://localhost:5173 ;; # Mac
            Linux*)     xdg-open http://localhost:5173 ;; # Linux
            MINGW*)     start http://localhost:5173 ;; # Windows
        esac
        break
    fi
    echo "‚è≥ Starting up... (attempt $attempt/$max_attempts)"
    sleep 2
    attempt=$((attempt + 1))
done

if [ $attempt -gt $max_attempts ]; then
    echo "‚ùå Application failed to start within the timeout period"
    cleanup
    exit 1
fi

# Keep the script running and showing logs
docker-compose logs -f
